void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef * htim) {
    if (htim->Instance == TIM3) {
        msCount--;
        if (msCount <= 0) {
            msCount = 1000;
        }
        if (msCount % 5 == 0) {
            buttonDebounce();
        }
        if (msCount % 2 == 0) {
            debounce = 1;
        }
    }
}

void buttonDebounce() {
    uint32_t current = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) & 1;
    buttonPress = !buttonState0 && current;
    buttonState0 = current;
}
